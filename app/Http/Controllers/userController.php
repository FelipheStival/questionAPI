     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function photo($id)
    {
        //Getting user
        $path = User::find($id)->profile_image;
        $exists = Storage::disk('public')->exists($path);

        if ($exists) {
            return Storage::download($path);
        } else {
            abort(404);
        }
    }
